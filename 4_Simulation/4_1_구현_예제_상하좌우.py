# N = 5
# plan = ['R', 'R', 'R', 'U', 'D', 'D']
N = int(input())
plan = input().split()
# ---------------------------------------------------

coor = [1, 1]
dic = {'R':(1, 1), 'L':(1, -1), 'U':(0, -1), 'D':(0, 1)}

# print(help(dic))
for i in plan:
    coor[dic[i][0]] += dic[i][1]
    for j in range(len(coor)):
        coor[j] = max(coor[j], 1)
        coor[j] = min(coor[j], N)

# ---------------------------------------------------
# output 형태 : 문제에서 원하는 형태는 list형태가 아닌, 공백으로 나눠진 x y 좌표 형태
# ---------------------------------------------------
# print(coor)
print(coor[0], coor[1])
